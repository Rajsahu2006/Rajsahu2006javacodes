//Find Factroial
public class Q5 {
    public static void main(String[] args) {
        int n =5;
        int i=1;
        int Factroial = 1;
        while (i<=n) {  //while loop
            
        
        for(int i =1;i<=n;i++){ //for loop
            System.out.println("Factorial of n");
            Factroial *=i;
            i++;
            System.out.println(Factroial);
        }}
        System.out.println(Factroial);
        
    }
    
}
